
public class Fichier extends Systeme {

	    // variables
	    private int taille;
	    private String nom;

	    //constructeur
	    public Fichier(String nom, int taille)
	    {
	        // initialise instance variables
	        this.nom = nom;
	        this.taille = taille;
	    }
	    
	     
	    
	    public int Taille()
	    {
	        return this.taille;
	    }
	}

